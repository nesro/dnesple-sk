<h1>cEDH games in Czechia</h1>
<p>
	cEDH, aka competitive Commander, is a very fun and interactive Magic the Gathering format that is
	played a lot in Czechia.
</p>
<p>
	I struggled to find people to play with, but to my surprise, there is a lot of small communities
	that play a lot! The goal of this website is to provide a summary of tournaments, friendly pub
	games or online games of cEDH. If you want to add your events to the calendar or have a link to
	your group here, please contact me.
</p>

